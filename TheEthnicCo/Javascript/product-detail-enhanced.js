// Buy Now Functionality
function initBuyNow() {
  const buyNowBtn = document.querySelector('.btn-buy-now');
  const qtyIncrease = document.querySelector('.qty-increase');
  const qtyDecrease = document.querySelector('.qty-decrease');
  const qtyInput = document.getElementById('quantity');
  
  if (qtyIncrease) {
    qtyIncrease.onclick = () => {
      const current = parseInt(qtyInput.value);
      if (current < 10) {
        qtyInput.value = current + 1;
        updateTotalPrice();
      }
    };
  }
  
  if (qtyDecrease) {
    qtyDecrease.onclick = () => {
      const current = parseInt(qtyInput.value);
      if (current > 1) {
        qtyInput.value = current - 1;
        updateTotalPrice();
      }
    };
  }
  
  if (buyNowBtn) {
    buyNowBtn.onclick = () => {
      if (!currentProduct) return;
      
      const quantity = parseInt(qtyInput?.value || 1);
      const selectedSize = document.querySelector('.size-btn.active')?.dataset.size;
      
      const orderData = {
        id: currentProduct.id,
        name: currentProduct.name,
        price: currentProduct.price,
        category: currentProduct.category,
        img: currentProduct.img,
        quantity: quantity,
        size: selectedSize || 'M'
      };
      
      localStorage.setItem('buyNowProduct', JSON.stringify(orderData));
      window.location.href = 'checkout.html';
    };
  }
  
  updateTotalPrice();
}

// Initialize Buy Now on page load
window.addEventListener('DOMContentLoaded', () => {
  initBuyNow();
});


// Update total price based on quantity
function updateTotalPrice() {
  const qtyInput = document.getElementById('quantity');
  const priceElement = document.getElementById('product-detail-price');
  const totalPriceElement = document.getElementById('total-price');
  
  if (qtyInput && priceElement && totalPriceElement) {
    const quantity = parseInt(qtyInput.value);
    const priceText = priceElement.textContent;
    const priceNum = parseFloat(priceText.replace(/[^0-9]/g, ''));
    const total = priceNum * quantity;
    totalPriceElement.textContent = `₹${total.toLocaleString()}`;
  }
}
