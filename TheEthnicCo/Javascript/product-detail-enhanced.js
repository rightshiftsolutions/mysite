let autoSlideInterval = null;
let isAutoSliding = true;
const DELIVERY_THRESHOLD = 2000;
const DELIVERY_CHARGE = 0;

// Auto-slide functionality
function startAutoSlide() {
  if (
    !currentProduct ||
    !currentProduct.images ||
    currentProduct.images.length <= 1
  )
    return;

  autoSlideInterval = setInterval(() => {
    if (isAutoSliding) {
      nextImage();
    }
  }, 4000);
}

function stopAutoSlide() {
  if (autoSlideInterval) {
    clearInterval(autoSlideInterval);
    autoSlideInterval = null;
  }
}

function pauseAutoSlide() {
  isAutoSliding = false;
}

function resumeAutoSlide() {
  isAutoSliding = true;
}

function loadBagFromStorage() {
  const saved = localStorage.getItem("shoppingBag");
  if (saved) {
    shoppingBag = JSON.parse(saved);
    updateBagCount();
  }
}

function saveBagToStorage() {
  localStorage.setItem("shoppingBag", JSON.stringify(shoppingBag));
}

function updateBagCount() {
  const count = shoppingBag.reduce((sum, item) => sum + item.quantity, 0);
  document.getElementById("cart-count").textContent = count;
  const headerCount = document.getElementById("cart-count-header");
  if (headerCount) headerCount.textContent = count;
}

function getDeliveryDate() {
  const today = new Date();
  const deliveryDays = 7;
  const deliveryDate = new Date(today.setDate(today.getDate() + deliveryDays));
  return deliveryDate.toLocaleDateString("en-IN", {
    month: "short",
    day: "numeric",
    year: "numeric",
  });
}

function applyPromo() {
  const promoInput = document.getElementById("promo-code");
  const code = promoInput.value.trim().toUpperCase();

  const promoCodes = {
    ETHNIC10: 10,
    WELCOME15: 15,
    FESTIVE20: 20,
  };

  if (promoCodes[code]) {
    promoCode = { code, discount: promoCodes[code] };
    promoInput.style.borderColor = "#27ae60";
    updateCartDisplay();
    setTimeout(() => {
      promoInput.value = "";
      promoInput.style.borderColor = "";
    }, 1000);
  } else {
    promoInput.style.borderColor = "#e74c3c";
    setTimeout(() => {
      promoInput.style.borderColor = "";
    }, 1000);
  }
}

function getProductIdFromURL() {
  const params = new URLSearchParams(window.location.search);
  return parseInt(params.get("id"));
}

function getCategorySlug(category) {
  return category.toLowerCase().replace(/\s+/g, "-");
}

function renderImageGallery(product) {
  const images = product.images || [product.img];
  const thumbnailsContainer = document.getElementById("gallery-thumbnails");

  thumbnailsContainer.innerHTML = "";
  images.forEach((img, index) => {
    const thumb = document.createElement("img");
    thumb.src = img;
    thumb.alt = `${product.name} ${index + 1}`;
    thumb.className = `gallery-thumb ${index === 0 ? "active" : ""}`;
    thumb.loading = "lazy";
    thumb.onclick = () => changeImage(index);
    thumbnailsContainer.appendChild(thumb);
  });

  document.getElementById("product-main-img").src = images[0];
  currentImageIndex = 0;
}

function changeImage(index) {
  const images = currentProduct.images || [currentProduct.img];
  currentImageIndex = index;

  document.getElementById("product-main-img").src = images[index];

  document.querySelectorAll(".gallery-thumb").forEach((thumb, i) => {
    thumb.classList.toggle("active", i === index);
  });
}

function nextImage() {
  const images = currentProduct.images || [currentProduct.img];
  currentImageIndex = (currentImageIndex + 1) % images.length;
  changeImage(currentImageIndex);
}

function prevImage() {
  const images = currentProduct.images || [currentProduct.img];
  currentImageIndex = (currentImageIndex - 1 + images.length) % images.length;
  changeImage(currentImageIndex);
}

